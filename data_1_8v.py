    dict_data = {}
    bit_distances = [0, 4, 8, 12, 16, 20, 24, 28]  # Assuming these are the bit distances for the 8 columns

    for adder, measurements in raw_data.items():
        measurements = np.array(measurements).reshape(-1, 9)
        bypass_times = measurements[:, 0]  # First column is bypass time
        delay_times = measurements[:, 1:]  # The rest are delay measurements

        adder_data = []
        for i, column in enumerate(delay_times.T):
            # Calculate adder_delay for each measurement
            transformed_delays = 1000000000 * 0.2005413157 / (2 * column) - \
                                 1000000000 * 0.2005413157 / (2 * bypass_times)
            mean_adder_delay = np.mean(transformed_delays)
            error_adder_delay = np.std(transformed_delays, ddof=1) / np.sqrt(len(transformed_delays))

            adder_data.append({
                "bit_distance": bit_distances[i],
                "adder_delay_mean": mean_adder_delay,
                "adder_delay_error": error_adder_delay
            })

        dict_data[adder] = adder_data

    return dict_data


